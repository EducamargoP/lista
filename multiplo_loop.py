"""Crie uma lista com os seguintes valores: [10, 20, 30, 40, 50, 60]

Utilizando um loop for, multiplique cada valor da lista por 2

Exiba a nova lista com os valores modificados"""

lista = [10, 20, 30, 40, 50, 60]

modificada = []
for numero in lista:
    modificada.append(numero * 2)

print(modificada)